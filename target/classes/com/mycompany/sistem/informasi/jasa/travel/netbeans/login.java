    private void checkLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkLoginActionPerformed
      try {
        String username = txt_name.getText();
        String password = txt_pass.getText();
       
            Connection conn=(Connection)koneksi.koneksiDB();
            Statement stt=conn.createStatement();
        if (conn != null) {
            
            //query ketable user
            String sql= "SELECT * FROM user WHERE username='"+username+"' AND password='"+password+"'";                
            ResultSet rs = stt.executeQuery(sql);
            
            if(rs.next()){
               usersession.set_nama(rs.getString("username"));
               new Menu().setVisible(true);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Username dan password yang anda masukkan salah!","Error",JOptionPane.ERROR_MESSAGE);
            }  
        }else{
              JOptionPane.showMessageDialog(this, "Tidak dapat terhubung ke database.", "Error", JOptionPane.ERROR_MESSAGE);
      
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } finally {
        try {
            if (rs != null) {
                rs.close();
            }
        } catch (SQLException ex) {
            // Handle the exception if needed
        }
    }
            
    }//GEN-LAST:event_checkLoginActionPerformed

